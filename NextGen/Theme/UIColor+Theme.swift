extension UIColor {
    
    static func themePrimaryColor() -> UIColor {
        return UIColor(netHex: 0xffcd14)
    }
    
    static func themeLightGreyColor() -> UIColor {
        return UIColor(netHex: 0x999999)
    }
    
}